public class CharArithDemo {
    public static void main (String[] args){
        char ch;
        ch ='l';
        System.out.println ("ch равно" +ch);
        ch++;
        System.out.println("Значение ch изменилось на" +ch);
        ch ='Д';
        ch--;
        System.out.println("Значение ch изменилось и равно"+ch);
    }
}
